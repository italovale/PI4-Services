package br.com.senac.pi4.entity;

public class Resultado {

	private String nmGrupo;
	private Integer pontosGrupo;
	private Integer pontosTotal;
	
	public String getNmGrupo() {
		return nmGrupo;
	}
	public void setNmGrupo(String nmGrupo) {
		this.nmGrupo = nmGrupo;
	}
	public Integer getPontosGrupo() {
		return pontosGrupo;
	}
	public void setPontosGrupo(Integer pontosGrupo) {
		this.pontosGrupo = pontosGrupo;
	}
	public Integer getPontosTotal() {
		return pontosTotal;
	}
	public void setPontosTotal(Integer pontosTotal) {
		this.pontosTotal = pontosTotal;
	}
	
}
